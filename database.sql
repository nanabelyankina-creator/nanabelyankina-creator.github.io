CREATE DATABASE IF NOT EXISTS ittender_db CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE ittender_db;

DROP TABLE IF EXISTS quote_request_items;
DROP TABLE IF EXISTS quote_requests;
DROP TABLE IF EXISTS feedback_messages;
DROP TABLE IF EXISTS products;
DROP TABLE IF EXISTS site_pages;
DROP TABLE IF EXISTS site_contacts;
DROP TABLE IF EXISTS users;

CREATE TABLE users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    login VARCHAR(100) NOT NULL UNIQUE,
    role VARCHAR(20) NOT NULL DEFAULT 'client',
    password_hash VARCHAR(255) NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE products (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    sku VARCHAR(100) NOT NULL UNIQUE,
    short_description TEXT,
    full_description TEXT,
    specifications TEXT,
    price DECIMAL(12,2) NOT NULL DEFAULT 0,
    image_path VARCHAR(255) NOT NULL DEFAULT '',
    is_visible TINYINT(1) NOT NULL DEFAULT 1,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE quote_requests (
    id INT AUTO_INCREMENT PRIMARY KEY,
    org_name VARCHAR(255) NOT NULL,
    inn VARCHAR(20) DEFAULT '',
    contact_person VARCHAR(255) NOT NULL,
    phone VARCHAR(100) NOT NULL,
    email VARCHAR(255) NOT NULL,
    comment TEXT,
    status VARCHAR(50) NOT NULL DEFAULT 'Новая',
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE quote_request_items (
    id INT AUTO_INCREMENT PRIMARY KEY,
    request_id INT NOT NULL,
    product_id INT NOT NULL,
    product_name VARCHAR(255) NOT NULL,
    price DECIMAL(12,2) NOT NULL,
    qty INT NOT NULL,
    FOREIGN KEY (request_id) REFERENCES quote_requests(id) ON DELETE CASCADE
);

CREATE TABLE feedback_messages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    contact VARCHAR(255) NOT NULL,
    message TEXT NOT NULL,
    created_at DATETIME DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE site_pages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    slug VARCHAR(100) NOT NULL UNIQUE,
    title VARCHAR(255) NOT NULL,
    content TEXT NOT NULL
);

CREATE TABLE site_contacts (
    id INT PRIMARY KEY,
    address VARCHAR(255) NOT NULL,
    phone VARCHAR(100) NOT NULL,
    email VARCHAR(255) NOT NULL
);

